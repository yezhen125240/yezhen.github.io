package net.shop.controller.admin;

import javax.annotation.Resource;

import net.shop.service.PluginService;
import net.shop.service.UploadTaskService;

import org.springframework.stereotype.Controller;
import org.springframework.ui.ModelMap;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;

/**
 * Controller - 存储插件
 * 
 * @author SHOP++ Team
 * @version 4.0
 */
@Controller("adminStoragePluginController")
@RequestMapping("/admin/storage_plugin")
public class StoragePluginController extends BaseController {

	@Resource(name = "pluginServiceImpl")
	private PluginService pluginService;
	@Resource(name = "uploadTaskServiceImpl")
	private UploadTaskService uploadTaskService;


	/**
	 * 列表
	 */
	@RequestMapping(value = "/list", method = RequestMethod.GET)
	public String list(ModelMap model) {
		model.addAttribute("storagePlugins", pluginService.getStoragePlugins());
		model.addAttribute("uploadTasks",uploadTaskService.findAll());
		return "/admin/storage_plugin/list";
	}

}